package main

import "text/template"

type ErrorCasesGenerator struct{}

func (g ErrorCasesGenerator) Template() *template.Template {
	return errorCasesTmpl
}

func (g ErrorCasesGenerator) Data() any {
	return struct {
		Pkgs           []string
		VarSets        [][]any
		InvalidChecks  []Check
		ValidChecks    []Check
		ValidNilChecks []Check
	}{
		Pkgs: []string{"assert", "require"},
		VarSets: [][]any{
			{"a"}, {"b.Get1()"}, {"c"}, {"errOp()"},
		},
		InvalidChecks: []Check{
			{Fn: "Nil", ArgsTmpl: "t, %s", ReportedMsgf: "use %s.%s", ProposedFn: "NoError"},
			{Fn: "NotNil", ArgsTmpl: "t, %s", ReportedMsgf: "use %s.%s", ProposedFn: "Error"},
		},
		ValidChecks: []Check{
			{Fn: "NoError", ArgsTmpl: "t, %s"},
			{Fn: "Error", ArgsTmpl: "t, %s"},
		},
		ValidNilChecks: []Check{
			{Fn: "Nil", ArgsTmpl: "t, ptr"},
			{Fn: "Nil", ArgsTmpl: "t, iface"},
			{Fn: "Nil", ArgsTmpl: "t, ch"},
			{Fn: "Nil", ArgsTmpl: "t, sl"},
			{Fn: "Nil", ArgsTmpl: "t, fn"},
			{Fn: "Nil", ArgsTmpl: "t, m"},
			{Fn: "Nil", ArgsTmpl: "t, uPtr"},

			{Fn: "NotNil", ArgsTmpl: "t, ptr"},
			{Fn: "NotNil", ArgsTmpl: "t, iface"},
			{Fn: "NotNil", ArgsTmpl: "t, ch"},
			{Fn: "NotNil", ArgsTmpl: "t, sl"},
			{Fn: "NotNil", ArgsTmpl: "t, fn"},
			{Fn: "NotNil", ArgsTmpl: "t, m"},
			{Fn: "NotNil", ArgsTmpl: "t, uPtr"},
		},
	}
}

var errorCasesTmpl = template.Must(template.New("errorCasesTmpl").
	Funcs(template.FuncMap{
		"ExpandCheck": ExpandCheck,
	}).
	Parse(`// Code generated by testifylint/internal/cmd/testgen. DO NOT EDIT.

package basic

import (
	"io"
	"testing"
	"unsafe"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

func TestErrorAsserts(t *testing.T) {
	errOp := func() error { return io.EOF }
	
	var (
		ptr   *int
		iface interface{}
		ch    chan error
		sl    []error
		fn    func()
		m     map[int]int
		uPtr unsafe.Pointer
	)
	
	var a error
	var b withErroredMethod
	_, c := b.Get2()
	{{ range $pi, $pkg := .Pkgs }}
	t.Run("{{ $pkg }}", func(t *testing.T) {
		{{- range $vi, $vars := $.VarSets }}
		{
			{{- range $ci, $check := $.InvalidChecks }}
			{{ ExpandCheck $check $pkg $vars }}
			{{ end }}}
		{{ end }}
		// Valid {{ $pkg }}s.
		{{ range $vi, $vars := $.VarSets }}
		{
			{{- range $ci, $check := $.ValidChecks }}
			{{ ExpandCheck $check $pkg $vars }}
			{{ end }}}
		{{ end }}
		{
		{{- range $ci, $check := $.ValidNilChecks }}
			{{ ExpandCheck $check $pkg nil }}
		{{ end }}}
	})
	{{ end }}}

type withErroredMethod struct{}

func (withErroredMethod) Get1() error        { return nil }
func (withErroredMethod) Get2() (int, error) { return 0, nil }
`))
